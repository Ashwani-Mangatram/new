package assignments;

public class If_block_99 
{
	
public static void main(String[] args) 
{
	int a = 1;
	
	
	if(a<10) 
	{
		System.out.println("smaller");
	}
	if(a>10) 
	{
		System.out.println("greater");
	}
	if(a==10) 
	{
		System.out.println("equal");
	}
	if(a!=10) 
	{
		System.out.println("not equal");
	}
	
}
}
