public class Operators_103 
{
    public static void main(String[] args) 
    {
		
    	int a = 10;
    	int b = 20;
    	
    	if (a==b && b == 10) 
    	{
    		System.out.println("1");
    	}
    	
    	if(a==10 || b==5) 
    	{
    		System.out.println("2");
    	}	
    	
    	if (!(b>6 && a==b)) 
    	{
    		System.out.println("3");
    	}
    	if (!(a==10 || b==20)) 
    	{
    		System.out.println("4");
    	}
	}
}
